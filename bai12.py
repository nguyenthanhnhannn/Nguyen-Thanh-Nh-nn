import random
# số nguyên
print("Số nguyên ngẫu nhiên từ 0 đến 100:", random.randint(0, 100))
print("Số nguyên ngẫu nhiên từ 50 đến 99:", random.randint(50, 99))
print("Số nguyên ngẫu nhiên từ -39 đến 79:", random.randint(-39, 79))
print("Số nguyên ngẫu nhiên từ -79 đến -39:", random.randint(-79, -39))
# số thực
print("Số thực ngẫu nhiên từ 0 đến 100:", random.uniform(0, 100))
print("Số thực ngẫu nhiên từ 50 đến 99:", random.uniform(50, 99))
print("Số thực ngẫu nhiên từ -39 đến 79:", random.uniform(-39, 79))
print("Số thực ngẫu nhiên từ -79 đến -39:", random.uniform(-79, -39))