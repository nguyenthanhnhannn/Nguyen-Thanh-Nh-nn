a = int(input("Nhập số nguyên a: "))
b = int(input("Nhập số nguyên b: "))
c = int(input("Nhập số nguyên c: "))
d = int(input("Nhập số nguyên d: "))
nho = a 
if b < nho:
    nho = b
if c < nho: 
    nho = c
if d < nho:
    nho = d
print("Số nguyên nhỏ nhất: ", nho)
        